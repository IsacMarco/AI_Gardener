const I18nContext = createContext<I18nContextValue | undefined>(undefined);

const getBaseLanguage = (language: LanguageCode): BaseLanguage => {
  if (language.toLowerCase().startsWith("es")) {
    return "es";
  }
  if (language.toLowerCase().startsWith("fr")) {
    return "fr";
  }
  if (language.toLowerCase().startsWith("de")) {
    return "de";
  }
  if (language.toLowerCase().startsWith("it")) {
    return "it";
  }
  if (language.toLowerCase().startsWith("pt")) {
    return "pt";
  }
  if (language.toLowerCase().startsWith("ro")) {
    return "ro";
  }
  return "en";
};

const resolveLanguageFromLocale = (locale: string): LanguageCode => {
  const normalizedLocale = locale.toLowerCase();

  const exactMatch = LANGUAGE_OPTIONS.find(
    (option) => option.code.toLowerCase() === normalizedLocale,
  );
  if (exactMatch) {
    return exactMatch.code;
  }

  const baseCode = normalizedLocale.split("-")[0];
  const sameBaseMatch = LANGUAGE_OPTIONS.find((option) =>
    option.code.toLowerCase().startsWith(`${baseCode}-`),
  );

  return sameBaseMatch?.code || "en-US";
};

const interpolate = (template: string, params?: TranslationParams) => {
  if (!params) return template;

  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = params[key];
    return value == null ? `{${key}}` : String(value);
  });
};

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [language, setLanguageState] = useState<LanguageCode>("en-US");

  useEffect(() => {
    const loadSavedLanguage = async () => {
      try {
        const savedLanguage = await AsyncStorage.getItem(LANGUAGE_STORAGE_KEY);
        const known = LANGUAGE_OPTIONS.find((option) => option.code === savedLanguage);
        if (known) {
          setLanguageState(known.code);
          return;
        }

        const deviceLocale =
          Intl.DateTimeFormat().resolvedOptions().locale || "en-US";
        const resolvedLanguage = resolveLanguageFromLocale(deviceLocale);
        setLanguageState(resolvedLanguage);
        await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, resolvedLanguage);
      } catch (error) {
        console.warn("Failed to load saved language", error);
      }
    };

    void loadSavedLanguage();
  }, []);

  const setLanguage = async (nextLanguage: LanguageCode) => {
    setLanguageState(nextLanguage);
    try {
      await AsyncStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
    } catch (error) {
      console.warn("Failed to persist selected language", error);
    }
  };

  const t = useMemo(() => {
    return (key: string, params?: TranslationParams) => {
      const base = getBaseLanguage(language);
      const template =
        TRANSLATIONS[base][key] || TRANSLATIONS.en[key] || key;
      return interpolate(template, params);
    };
  }, [language]);

  const contextValue = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      languageOptions: LANGUAGE_OPTIONS,
      t,
    }),
    [language, t],
  );

  return <I18nContext.Provider value={contextValue}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
};
